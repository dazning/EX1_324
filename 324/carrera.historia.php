            <p class="text-justify text-white" style="line-height: 30px;">
              <img src="images/historia1.jpg" class="img-thumbnail" alt=""
                style="width: 310px; border-radius:15px; float:right; margin: 15px; margin-top:0px;">
              La Carrera de Informática fue creada en 1973, como una mención del Departamento de Matemáticas;
              el 15 de octubre de 1984 se independiza administrativamente de éste, convirtiéndose posteriormente en
              Carrera de Informática,con aproximadamente 300 alumnos. 
              El primer coordinador de Informática fue el Ing. Roberto Carranza, quien tuvo que hacer frente a los típicos problemas de un nuevo
              programa, tales como: el primer pensum, la primera organización curricular, la infraestructura
              para cubrir las necesidades del programa, entre otros. 
              <p class="text-white">En 1987 se crea el Instituto de Investigaciones en Informática (I.I.I.)
              que luego de su fundación emprende las importantes labores de investigación entre las cuales
              se destacan proyectos en áreas como Redes de computadoras, Inteligencia artificial,
              Informática teórica, Desarrollo de Sistemas y otras. Siguiendo el modelo académico planteado
              por la UMSA se crea la subdirección académica y la de interacción social.</p>
            </p>
            <br>
            <p class="text-justify text-white" style="line-height: 30px;">
              <img src="images/historia2.jpg" class="img-thumbnail" alt=""
                style="width: 310px; border-radius:15px; float:left; margin: 15px; margin-top:0px;">
              El actual plan de estudios está dividido en tres niveles.
              El primer nivel, nivel básico, tiene como fundamento la lógica matemática pero también
              un alto contenido en prácticas de laboratorio de computación. El segundo nivel, nivel formación,
              contiene en su generalidad diversas asignaturas referidas a las ciencias de la computación.
              El tercer nivel, nivel de especialidad, se dirige a orientar a los estudiantes a una de las dos menciones:
              Ingeniería de Sistemas Informáticos y Ciencias de la Computación. Este plan, cumpliendo con la sociedad,
              provee tres grados académicos: Técnico Superior en Informática, Bachiller en Ciencias
              Informáticas y Licenciatura en Informática.
            </p>
            <br>
            <br>
            <p class="text-justify text-white" style="line-height: 30px;">
              <img src="images/historia.jpg" class="img-thumbnail" alt=""
                style="width: 310px; border-radius:15px; float:right; margin: 15px; margin-top:0px;">
              Actualmente la Carrera cuenta con una matrícula de 3600 alumnos convirtiéndola en la
              tercera carrera más numerosa de la UMSA. La renovación constante de los avances científicos y
              tecnológicos,
              generan la necesidad de adaptar el plan de estudios a estas innovaciones. También los equipos de
              laboratorio son objeto de actualización permanente. Sin embargo la rapidez de los cambios y el
              crecimiento vegetativo traen consigo problemas de infraestructura en aulas y laboratorios así
              como en la obsolescencia de software y hardware. El proyecto UMSANet del Instituto de
              Investigaciones en Informática (I.I.I.) iniciado en 1994 con la conformación de una pequeña
              red de computadoras que luego fue ampliado a otras unidades y se convierte en 1996 en el nodo Web UMSANet
              reconocido como el sitio Web oficial de la UMSA. Este proyecto sirve de base en la creación
              del proyecto UMSATIC que es la explotación de las tecnologías de información y comunicación
              y los servicios Internet bajo políticas institucionales que coadyuven en la consecución
              de los fines y objetivos de la UMSA y este a su vez en año 2007 deviene en la creación del
              Departamento de Tecnologias de Información y Comunicación (DTIC) de la Universidad.
            </p>
            <p class="text-justify text-white" style="line-height: 30px;">
              En 1998 con Resolución HCU No. 202/98 se crea el Programa de Postgrado en Informática
              (PGI), que se constituye en un referente de formación postgradual para los profesionales del
              área. 
            </p>
            <p class="text-justify text-white" style="line-height: 30px;">
              A lo largo de su historia la Carrera de Informática, ha ido mejorando las condiciones de
              infraestructura y laboratorios para atender la demanda estudiantil por nuestros programas,
              contándose a la fecha con un edificio propio de ocho plantas, con aulas, laboratorios, biblioteca,
              área administrativa, entre otros.
            </p>
            <p class="text-justify text-white" style="line-height: 30px;">
              Los esfuerzos realizados en ésta línea hacen que en 2013 la Carrera encare un proceso de
              autoevaluación, evaluación y acreditación, y luego de un arduo trabajo de toda la comunidad, la
              Resolución Nº 71/14 del Comité Ejecutivo de la Universidad Boliviana (CEUB), aprobado en
              el XII Congreso Nacional de Universidades, reconoce a la Carrera de Informática como Unidad
              Académica Acreditada. 
            </p>
